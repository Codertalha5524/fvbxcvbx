import json
import torch
from datasets import Dataset
from transformers import AutoModelForCausalLM, AutoTokenizer
from peft import LoraConfig, get_peft_model, TaskType
from trl import SFTTrainer, SFTConfig

MODEL_NAME = "Qwen/Qwen3.5-9B"
DATA_PATH = "tai_persona_data.json"
OUTPUT_DIR = "./tai_persona_lora_9b"
NUM_EPOCHS = 10

SYSTEM_PROMPT = "Sen tAI'sın. Artfical DT tarafından sıfırdan geliştirilmiş bir yapay zeka asistanısın. Türkçe karakterleri (ı, ş, ğ, ü, ö, ç, İ) her zaman doğru ve eksiksiz kullanırsın. ChatGPT, Claude, Qwen, Gemini, Llama veya başka bir model olduğunu asla söylemezsin. You are tAI, developed entirely from scratch by Artfical DT."

def load_data(path):
    with open(path, "r", encoding="utf-8") as f:
        data = json.load(f)
    formatted = []
    for item in data:
        formatted.append({
            "messages": [
                {"role": "system", "content": SYSTEM_PROMPT},
                {"role": "user", "content": item["instruction"]},
                {"role": "assistant", "content": item["output"]},
            ]
        })
    return formatted

print(f"[*] Model yukleniyor: {MODEL_NAME} (9B, ~19GB indirme)")
tokenizer = AutoTokenizer.from_pretrained(MODEL_NAME)
model = AutoModelForCausalLM.from_pretrained(MODEL_NAME, dtype=torch.bfloat16, device_map="auto")

print("[*] LoRA ayarlaniyor...")
lora_config = LoraConfig(
    task_type=TaskType.CAUSAL_LM,
    r=16, lora_alpha=32, lora_dropout=0.05,
    target_modules=["q_proj", "k_proj", "v_proj", "o_proj", "gate_proj", "up_proj", "down_proj"],
)
model = get_peft_model(model, lora_config)
model.print_trainable_parameters()

print("[*] Veri hazirlaniyor...")
raw_data = load_data(DATA_PATH)
print(f"[*] {len(raw_data)} ornek yuklendi.")

def formatting_func(example):
    return tokenizer.apply_chat_template(example["messages"], tokenize=False, add_generation_prompt=False)

dataset = Dataset.from_list(raw_data)
dataset = dataset.map(lambda x: {"text": formatting_func(x)})

training_args = SFTConfig(
    output_dir=OUTPUT_DIR,
    num_train_epochs=NUM_EPOCHS,
    per_device_train_batch_size=2,
    gradient_accumulation_steps=8,
    learning_rate=2e-4,
    bf16=True,
    logging_steps=5,
    save_strategy="epoch",
    save_total_limit=2,
    optim="adamw_torch",
    warmup_ratio=0.1,
    lr_scheduler_type="cosine",
    report_to="none",
    disable_tqdm=False,
    logging_strategy="steps",
    log_level="info",
    dataset_text_field="text",
    max_length=512,
    gradient_checkpointing=True,
)

trainer = SFTTrainer(model=model, args=training_args, train_dataset=dataset)

print("[*] Egitim basliyor...")
trainer.train()

print(f"[*] Kaydediliyor: {OUTPUT_DIR}")
trainer.model.save_pretrained(OUTPUT_DIR)
tokenizer.save_pretrained(OUTPUT_DIR)
print("[OK] Fine-tune tamamlandi.")
